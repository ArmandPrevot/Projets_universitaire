package card;

/**
 * The enum ImmediateEffect
 * 
 *	Immediate effect that trigger when a player buys a favor
 *
 */

public enum ImmediateEffect {
	
	/** Immediate effect that adds gold to the player's ressources */
	INSTANT_GOLD,
	
	/** Immediate effect that adds solar point to the player's ressources */
	INSTANT_SOLAR,
	
	/** Immediate effect that adds solar point to the player's ressources */
	INSTANT_LUNAR,
	
	/** No immediate effect */
	NONE;
	

}
